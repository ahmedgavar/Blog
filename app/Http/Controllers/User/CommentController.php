     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate(['comment' => 'required'], ['comment.required' => 'comment cannot be empty']);

        $comment = Comment::create(['comment' => $request->comment, 'post_id' => $request->post_id]);

        broadcast(new NewComment($comment->user, $comment));

        // return $comment;
        return response()->json(['status' => 200, 'message' => "comment saved successfully"]);
    }

